    private void bnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnTimActionPerformed
        // TODO add your handling code here:
        String col[] = {"Mã KH", "Tên KH", "Giới Tính", "Ngày Sinh", "SĐT"};
        ArrayList<KhachHang> t = x.Timkiem(txtTim.getText());
        DefaultTableModel dtm = new DefaultTableModel(col, 0);
        for (int i = 0; i < t.size(); i++) {
            Object[] obj = {t.get(i).getMaKhachHang(), t.get(i).getTenKhachHang(), t.get(i).getGioiTinh(), t.get(i).getNgaySinh(), t.get(i).getSoDienThoai()};
            dtm.addRow(obj);

        }
        tbDSKH.setModel(dtm);
        tbDSKH.updateUI();
    }//GEN-LAST:event_bnTimActionPerformed

