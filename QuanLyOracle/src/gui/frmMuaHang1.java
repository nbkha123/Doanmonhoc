    private void uploadDSHD() {
        String col[]={"Mã HD","Ngày Tạo","Tổng Tiền"};
        ArrayList<HoaDon> t = x.LayDSHD();
        DefaultTableModel dtm=new DefaultTableModel(col,0);
        for(int i=0;i<t.size();i++)
        {
            Object []obj={t.get(i).getMaHoaDon(),t.get(i).getNgayTao(),t.get(i).getTongTien()};
            dtm.addRow(obj);
            
        }
        TableDSHD.setModel(dtm);
        TableDSHD.updateUI();
    }
    
