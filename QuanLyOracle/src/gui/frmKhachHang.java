    private void bnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnThemActionPerformed
        // TODO add your handling code here:
        if (txtMaKH.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã khách hàng");
            return;
        }
        int kq = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm khách hàng mới không", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (kq == JOptionPane.YES_OPTION) {
            if (ktTrungMa(txtMaKH.getText()) == true) {
                JOptionPane.showMessageDialog(this, "Đã tồn tại mã khách hàng này. Vui lòng chọn mã khác ");
                return;
            }
            KhachHang kh = new KhachHang();
            kh.setMaKhachHang(txtMaKH.getText().trim());
            kh.setTenKhachHang(txtTenKH.getText().trim());
            kh.setGioiTinh(txtGT.getText().trim());
            kh.setNgaySinh(txtNS.getText().trim());
            kh.setSoDienThoai(txtSDT.getText().trim());

            x.Them(kh);
            updateTable();
            JOptionPane.showMessageDialog(this, "Bạn đã thêm thành công");
            xoatrang();
        }
    }//GEN-LAST:event_bnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (txtMaKH.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã khách hàng");
            return;
        }
        int kq = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa khách hàng này không", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (kq == JOptionPane.YES_OPTION) {
            if (ktTrungMa(txtMaKH.getText()) == false) {
                JOptionPane.showMessageDialog(this, "Không có mã khách hàng này. Vui lòng nhập mã khác ");
                return;
            }
            KhachHang kh = new KhachHang();
            kh.setMaKhachHang(txtMaKH.getText());
            x.Xoa(kh);
            updateTable();
            JOptionPane.showMessageDialog(this, "Bạn đã xóa thành công");
            xoatrang();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

