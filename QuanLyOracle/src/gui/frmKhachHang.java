    private void updateTable() {
        String col[] = {"Mã KH", "Tên KH", "Giới Tính", "Ngày Sinh", "SĐT"};
        ArrayList<KhachHang> t = x.LayDS();
        DefaultTableModel dtm = new DefaultTableModel(col, 0);
        for (int i = 0; i < t.size(); i++) {
            Object[] obj = {t.get(i).getMaKhachHang(), t.get(i).getTenKhachHang(), t.get(i).getGioiTinh(), t.get(i).getNgaySinh(), t.get(i).getSoDienThoai()};
            dtm.addRow(obj);

        }

        tbDSKH.setModel(dtm);
        tbDSKH.updateUI();
    }

    private void xoatrang() {

        txtGT.setText("");
        txtMaKH.setText("");
        txtNS.setText("");
        txtSDT.setText("");
        txtTenKH.setText("");
        txtMaKH.requestFocus();
    }

    private boolean ktTrungMa(String ma) {

        try {
            String sql = "select makh from khachhang where makh='" + ma + "'";
            TruyVan provider = new TruyVan();
            provider.open();
            ResultSet rs = provider.executeQuery(sql);
            if (rs.next()) {
                return true;
            }
            return false;

        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return false;
    }

