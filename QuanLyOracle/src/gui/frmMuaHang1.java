    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        String col[]={"Mã Sản Phẩm","Sản Phẩm","Số Lượng","Giá Bán","Loại Sản Phẩm"};
        ArrayList<SanPham> t=y.TimKiem(txtTim.getText());
        DefaultTableModel dtm=new DefaultTableModel(col,0);
        for(int i=0;i<t.size();i++)
        {

            Object []obj={t.get(i).getMaSanPham(),t.get(i).getTenSanPham(),t.get(i).getSoLuong(),t.get(i).getDonGiaBan(),t.get(i).getLoaiSanPham()};
            dtm.addRow(obj);

        }
        TableSP.setModel(dtm);
        TableSP.updateUI();
    }//GEN-LAST:event_btnTimActionPerformed

